
// Calculate area of cicle , triangle, rectangle and sqaure 

function areaOfCircle(radius1){
    console.log("Area of a Circle is : "+(3.14*radius1*radius1))
}

function areaOfTriangle(breadth,height){
    console.log("Area of a triangle is : "+(0.5*breadth*height))
}

function areaOfRectangle(length,width){
    console.log("Area of a Rectangle is : "+(length*width))
}

function areaOfSquare(side){
    console.log("Area of a Square : "+(side*side))
}

areaOfCircle(10);
areaOfTriangle(12,15);
areaOfRectangle(15,23);
areaOfSquare(89);